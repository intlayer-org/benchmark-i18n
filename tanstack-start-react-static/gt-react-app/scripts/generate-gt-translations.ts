const locales = ["en", "fr", "es", "de", "it", "pt", "zh", "ja", "ko", "ru"];

const en = {};
keys.forEach((k) => (en[k] = k));

import fs from "node:fs";

locales.forEach((locale) => {
  const translations = {};
  keys.forEach((k) => {
    if (locale === "en") {
      translations[k] = k;
    } else {
      translations[k] = `[${locale.toUpperCase()}] ${k}`;
    }
  });
  fs.writeFileSync(
    `./src/_gt/${locale}.json`,
    JSON.stringify(translations, null, 2)
  );
});
