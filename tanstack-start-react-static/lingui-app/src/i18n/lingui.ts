import { setupI18n } from "@lingui/core";

export const locales = [
  "en",
  "fr",
  "es",
  "de",
  "it",
  "pt",
  "zh",
  "ja",
  "ko",
  "ru",
] as const;
export type Locale = (typeof locales)[number];
export const defaultLocale: Locale = "en";

export function getLocaleName(locale: string): string {
  try {
    const displayNames = new Intl.DisplayNames([locale], { type: "language" });
    const name = displayNames.of(locale);
    return name ? name.charAt(0).toUpperCase() + name.slice(1) : locale;
  } catch (e) {
    return locale.toUpperCase();
  }
}


// Import compiled .js catalogs (generated by `lingui compile`).
// Using compiled files instead of raw .json avoids the "Uncompiled message detected"
// warning from @lingui/core, which appears when plain strings are loaded directly.
// import.meta.glob tells Vite about all locale files at build time so they are
// correctly included in both client and server bundles.
const messageModules = import.meta.glob("../locales/*/messages.mjs");

export async function loadMessages(locale: string) {
  const path = `../locales/${locale}/messages.mjs`;
  const moduleLoader =
    messageModules[path] ||
    messageModules[`../locales/${defaultLocale}/messages.mjs`];

  try {
    // lingui compile outputs ESM in .mjs files.
    const mod = (await moduleLoader()) as any;
    return mod.messages;
  } catch (error) {
    console.error(`Failed to load messages for locale: ${locale}`, error);
    const fallback = (await messageModules[
      `../locales/${defaultLocale}/messages.mjs`
    ]()) as any;
    return fallback.messages;
  }
}

export function initLingui(locale: string, messages: any) {
  const lingui = setupI18n();
  lingui.load(locale, messages);
  lingui.activate(locale);
  return lingui;
}
